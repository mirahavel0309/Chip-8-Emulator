#pragma once
#include <cstdint>

class Chip8 {
public:
    uint8_t  memory[4096];      // 4KB of RAM
    uint8_t  v[16];             // General purpose registers (V0 - VF)
    uint16_t index;             // Index register (I)
    uint16_t pc;                // Program counter
    uint16_t stack[16];         // Subroutine return addresses
    uint8_t  sp;                // Stack pointer
    uint8_t  delayTimer;        // 60Hz delay timer
    uint8_t  soundTimer;        // 60Hz sound timer (beeps when > 0)
    uint8_t  display[64 * 32];  // Graphics buffer (monochrome)
    uint8_t fontset[80] = {
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
    };

    Chip8();
    bool LoadROM(const char* filename); // Load binary into memory
    void Cycle();                       // Emulate one CPU cycle (Fetch-Decode-Execute)
};